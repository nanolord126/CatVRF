<?php
declare(strict_types=1);

$dir = "C:\opt\kotvrf\CatVRF\app\Domains";
$it = new RecursiveIteratorIterator(new RecursiveIteratorDirectoryIterator($dir));

foreach ($it as $file) {
    if ($file->isDir() || $file->getExtension() !== "php") continue;
    $path = $file->getPathname();
    $content = file_get_contents($path);
    
    // ???????: public function __construct(...) { ... public function __construct() { ... } }
    // ??????? ??????, ?????? ??? TODO-???? ??????????? ? ????????? ?????? ?????????
    $pattern = "/public function __construct\((.*?)\)\s*\{.*?public function __construct\(\)\s*\{.*?\}(?:\s*\})?/s";
    
    if (preg_match($pattern, $content)) {
        $newContent = preg_replace_callback($pattern, function($matches) {
            $params = $matches[1];
            // ?????????? ?????? ?????? ??????????? ? ????????????? ???????????
            // ?? ??? ????? ?????????, ??? ?? ?? ??????? ???????? ???.
            // ? ?????? ???????? ?????? ??????????? ?????? ??? ? TODO.
            return "public function __construct(" . $params . ")\n    {\n    }";
        }, $content);
        
        if ($newContent !== $content) {
            file_put_contents($path, $newContent);
            echo "FIXED nested construct: $path\n";
        }
    }
}
