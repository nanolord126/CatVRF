<?php

declare(strict_types=1);

namespace Modules\AIConstructor\Domain\Enums;

/**
 * Исчерпывающее перечисление строго регламентированных типов AI-результатов.
 * 
 * Категорически стандартизирует все возможные форматы вывода AI-конструктора для гарантированной 
 * консистентной обработки и визуализации на клиентской стороне.
 */
enum AIConstructionType: string
{
    /**
     * Сгенерированное изображение (например, результат работы Stable Diffusion / DALL-E / Vision).
     * Безупречно применяется для виртуальной примерки в Beauty или 3D-расстановки в Furniture.
     */
    case IMAGE = 'image';

    /**
     * Строго структурированный список рекомендаций или спецификаций.
     * Абсолютно незаменим для Fashion (капсулы) и Medical (предварительный план анализов).
     */
    case LIST = 'list';

    /**
     * Многомерный дизайнерский проект с привязкой конкретных координат.
     * Исключительно используется в RealEstate (виртуальный ремонт) и Interiors.
     */
    case DESIGN = 'design';

    /**
     * Строгий финансовый или количественный расчет, сгенерированный нейросетью.
     * Категорически применяется в Food (кастомизация рецептов) и Auto (смета запчастей).
     */
    case CALCULATION = 'calculation';
}
