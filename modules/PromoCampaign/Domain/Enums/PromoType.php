<?php

declare(strict_types=1);

namespace Modules\PromoCampaign\Domain\Enums;

/**
 * Перечисление исключительно допустимых типов промо-кампаний в системе.
 *
 * Категорически стандартизирует и строго детерминирует поддерживаемые виды акционных предложений,
 * скидок, подарочных сертификатов и бонусов, применяемых в рамках мультитенантной платформы.
 * Строгое соответствие канону: discount_percent, fixed_amount, bundle, buy_x_get_y, gift_card, referral_bonus, turnover_bonus.
 */
enum PromoType: string
{
    /**
     * Классическая процентная скидка, применяемая к итоговой сумме корзины или отдельного товара.
     * Абсолютно требует строгого контроля максимальной суммы скидки.
     */
    case DISCOUNT_PERCENT = 'discount_percent';

    /**
     * Фиксированная абсолютная скидка в копейках, вычитаемая из итоговой суммы заказа/бронирования.
     * Строго требует контроля минимальной суммы заказа для предотвращения отрицательных балансов.
     */
    case FIXED_AMOUNT = 'fixed_amount';

    /**
     * Акция типа "Бандл", предоставляющая фиксированную сниженную цену за строго определенный набор товаров.
     */
    case BUNDLE = 'bundle';

    /**
     * Акция "Купи X, получи Y", автоматически добавляющая бесплатные или сниженные по стоимости товары.
     */
    case BUY_X_GET_Y = 'buy_x_get_y';

    /**
     * Подарочный сертификат с фиксированным номиналом, который можно применить как средство частичной или полной оплаты.
     */
    case GIFT_CARD = 'gift_card';

    /**
     * Реферальный бонус, начисляемый или применяемый в рамках программы приглашения пользователей.
     */
    case REFERRAL_BONUS = 'referral_bonus';

    /**
     * Бонус за достижение определенного финансового оборота (например, после 50 000 руб).
     */
    case TURNOVER_BONUS = 'turnover_bonus';
}
