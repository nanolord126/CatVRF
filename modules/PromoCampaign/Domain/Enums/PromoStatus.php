<?php

declare(strict_types=1);

namespace Modules\PromoCampaign\Domain\Enums;

/**
 * Перечисление строго регламентированных статусов жизненного цикла промо-кампании.
 *
 * Категорически используется для детерминирования текущего операционного состояния акции.
 * Строго предотвращает применение недействительных, исчерпанных или приостановленных кампаний.
 */
enum PromoStatus: string
{
    /**
     * Промо-кампания абсолютно активна, валидна и полностью доступна для штатного применения пользователями.
     */
    case ACTIVE = 'active';

    /**
     * Промо-кампания принудительно и временно приостановлена администратором или бизнес-пользователем.
     */
    case PAUSED = 'paused';

    /**
     * Бюджет промо-кампании полностью и окончательно исчерпан, дальнейшее применение категорически исключено.
     */
    case EXHAUSTED = 'exhausted';

    /**
     * Срок действия промо-кампании безвозвратно и гарантированно истек.
     */
    case EXPIRED = 'expired';
}
